#Tipos int e float 
#int -> número inteiro
#int representa qualquer número positivo e negativo inteiro
print(11) #int
print(-11) #int
print(0) #int

#float -> Número com ponto flutuante
print(1.1, 0.0, -1.5)

#A "função" type mostra o tipo que o Python inferiu ao valor
print(type('Kenji'))
print(type(1))
print(type(1.2))