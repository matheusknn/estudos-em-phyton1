# \n -> LF
# sep define como os argumentos colocados da função prints serão separados o padrão é por espaço
# end define o que vem depois dos argumentos colocados na função print, o pdrão é uma quebra de linha(\n)
print(12, 34, sep=" - ", end='\n')
