# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# Escape
print("Luiz \"Otávio\"")
print("Matheus \"Kenji\"") #\ serve como escapa para o caractere que vier após ele

# r
print(r"Luiz \"Otávio\"") #faz o caractere de escape ser exibido